package bean;

public class HubInfo {
    String hubCpuModel;
    String hubCpuCore;
    String hubDiskTotal;
    String hubMemoryTotal;
    String hubOsArch;
    String hubOsVersion;
    String hubMacAddress;

    public String getHubCpuModel() {
        return hubCpuModel;
    }

    public void setHubCpuModel(String hubCpuModel) {
        this.hubCpuModel = hubCpuModel;
    }

    public String getHubCpuCore() {
        return hubCpuCore;
    }

    public void setHubCpuCore(String hubCpuCore) {
        this.hubCpuCore = hubCpuCore;
    }

    public String getHubDiskTotal() {
        return hubDiskTotal;
    }

    public void setHubDiskTotal(String hubDiskTotal) {
        this.hubDiskTotal = hubDiskTotal;
    }

    public String getHubMemoryTotal() {
        return hubMemoryTotal;
    }

    public void setHubMemoryTotal(String hubMemoryTotal) {
        this.hubMemoryTotal = hubMemoryTotal;
    }

    public String getHubOsArch() {
        return hubOsArch;
    }

    public void setHubOsArch(String hubOsArch) {
        this.hubOsArch = hubOsArch;
    }

    public String getHubOsVersion() {
        return hubOsVersion;
    }

    public void setHubOsVersion(String hubOsVersion) {
        this.hubOsVersion = hubOsVersion;
    }

    public String getHubMacAddress() {
        return hubMacAddress;
    }

    public void setHubMacAddress(String hubMacAddress) {
        this.hubMacAddress = hubMacAddress;
    }
}
